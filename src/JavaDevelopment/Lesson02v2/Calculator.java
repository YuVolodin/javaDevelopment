package JavaDevelopment.Lesson02v2;

public class Calculator {
    // методы суммы

    public static double Sum (double first, double second) {
        return first + second;
    }
    public static int Sum (int first, int second) {
        return first + second;
    }
    public static long Sum (long first, long second) {
        return first + second;
    }

    // методы умножения

    public static double Multiply (double first, double second) {
        return first * second;
    }
    public static int Multiply (int first, int second) {
        return first * second;
    }
    public static long Multiply (long first, long second) {
        return first * second;
    }

    // методы вычитания

    public static double Substraction (double first, double second) {
        return first - second;
    }
    public static int Substraction (int first, int second) {
        return first - second;
    }
    public static long Substraction (long first, long second) {
        return first - second;
    }
    
    // методы деления

    public static double Divide (double first, double second) {
        return first / second;
    }
    public static double Divide (int first, int second) {
        return (double) first / second;
    }
    public static double Divide (long first, long second) {
        return (double) first / second;
    }

}
